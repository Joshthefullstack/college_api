class Lecturer{
    constructor(
        lecturerId,
        departmentId,
        surname,
        firstName,
        otherName,
        staffId,
        status
    ){
        this.LecturerId = lecturerId;
        this.DepartmentId = departmentId;
        this.Surname = surname;
        this.FirstName = firstName;
        this.OtherName = otherName;
        this.StaffId = staffId;
        this.Status = status;
    }
}